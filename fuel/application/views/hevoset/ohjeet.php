<h2>1. Ennen rekisteröintiä</h2>

<h3><a name="lomake"></a>1.1. Rekisteröintilomakkeen täyttäminen ja käsittely</h3>

<p>Vain VRL-tunnuksen rekisteröineet jäsenet voivat rekisteröidä hevosiaan virtuaalihevosrekisteriin. Rekisteröintilomake  löytyy kohdasta Virtuaalihevoset > <a href="<?php echo base_url('virtuaalihevoset/rekisterointi')?>" title="Rekisteröi virtuaalihevonen">Hevosten rekisteröinti</a>.
</p>

<p>Hevoset lisätään automaattisesti hevosrekisteriin. Jos huomaat tehneesi virheen tai haluat perua rekisteröinnin, voit poistaa hevosen rekisteristä <b>vuorokauden (24 h) kuluessa</b> rekisteröinnistä hevosen omasta profiilista. Vuorokauden jälkeen pystyt yhä muokkaamaan hevosen omistajatietoja ja osoitetta, ruunaamaan orin, muuttamaan sen säkäkorkeutta ja väriä sekä lisäämään sille puuttuvia tietoja. Muiden tietojen muutokset onnistuvat ainoastaan <a href="<?php echo base_url('tuki')?>" title="Ota yhteyttä tukeen" target="_blank">tuen</a> kautta. </p>


<h3><a name="sivut"></a>1.2. Hevosen sivut</h3>

<p>Hevosella tulee olla oma verkkosivu, josta löytyvät selkeästi <b>suomen tai englannin kielellä</b> näissä ohjeissa mainitut pakolliset tiedot (nimi, rotu, sukupuoli, syntymäaika, omistaja) vaaditulla tavalla. Lisäksi hevosen sivuilla tulee olla maininta virtuaalisuudesta Lue kohta: <a href="#teksti">Virtuaalihevonen-merkintä</a>.</p>

<p>Mikäli hevosen tiedot löytyvät joukkosivulta tai esimerkiksi instagramista, tulee osoitteen viedä suoraan hevosen kohdalle (ankkurilinkki, linkki postaukseen).</p>

<p>Salasanasuojattujen sivujen tulee olla avattavissa käyttäjätunnuksella user ja salasanalla arabians.</p>

<p><b>Huomaathan, että rekisterissä ja hevosen sivulla ilmoitettujen tietojen on täsmättävä täydellisesti.</b></p>

<p>Sivulta ei saa löytyä laitonta materiaalia ja tekijänoikeudet tulee olla merkitty asianmukaisesti.</p>

<h3><a name="teksti"></a>1.3. Virtuaalihevonen-merkintä</h3>

<p>Hevosen sivulta tulee löytyä CTRL + F -selainhaulla <b>näkyvä, kirjoitettu teksti “virtuaalihevonen” tai “sim-game horse”</b> juuri tässä muodossa, esimerkiksi: "tämä on virtuaalihevonen". Merkintä otsikkokuvassa, muussa kuvassa, titlessä (sivun otsikossa) tai muu muoto, kuten virtuaaliponi/virtuaalinen hevonen/virtual horse, ei kelpaa.</p>

<p>Merkinnällä pyritään estämään virtuaalihevosten sekoittaminen oikeisiin hevosiin ja mahdollistamaan virtuaalihevosten poistaminen hakukoneiden hakutuloksista käyttämällä hakusanaa "virtuaalihevonen".</p>

<h3><a name="valvonta"></a>1.4. Rekisterin valvonta</h3>

<p>Hevosrekisterin työntekijät valvovat rekisteriin ilmoitettujen hevosten asiallisuutta sekä vaadittuja tietoja ja merkintöjä pistotarkastuksin. <b>Virheellisesti rekisteröityjen hevosten nimi muutetaan muotoon “VIRHEELLISET TIEDOT: hevosen nimi”</b> ja sen omistajaan otetaan yhteyttä VRL:on pikaviestillä sekä sähköpostitse. Omistajan korjattua puutteelliset tiedot voidaan hevosen nimi muuttaa alkuperäiseen muotoonsa. </p>

<p><b>Toistuvat virheelliset, korjaamattomat rekisteröinnit (3. huomautus) poistetaan 2 viikon kuluessa annetusta varoituksesta.</b></p>


<h2>2. Pakolliset tiedot</h2>

<p>Pakollisten tietojen tulee löytyä hevosen sivulta selkeästi suomen tai englannin kielellä. </p>

<h3><a name="nimi"></a>2.1. Nimi</h3>
<p>Nimen tulee olla asiallinen ja hyvän maun mukainen. Rekisteristä voidaan myöhemmin sensuroida nimiä, joissa on kirosanoja tai selkeästi huumeisiin, väkivaltaan, rasismiin, seksiin tai muuhun mauttomaan tai sopimattomaan materiaaliin viittaavia sanoja. Hevosen nimellä ei saa myöskään olla selkeitä yhteyksiä tosielämään, eli se ei saa olla sama kuin jollain tunnetulla hevosella.</p>
<p>Erikoismerkit, kuten aksenttimerkit tai kyrilliset aakkoset, ovat nimissä sallittuja.</p>
<p><b>Rekisteriin ei hyväksytä kahta täsmälleen samannimistä ja saman rotuista hevosta</b>. Kasvattajaliite tekee nimestä eri nimen, esimerkiksi gotlanninrussorit Dahlgårds Joakim ja Karkurannan Joakim ovat kaksi eri nimeä, samoin kuin Joakim ja Joakim II.</p>

<h3><a name="rotu"></a>2.2. Rotu ja sukupuoli</h3>
<p>Hevosen sivulta tulee löytyä maininta hevosen rodusta ja sukupuolesta.</p>

<p>Huomaathan, että hevosen rotua tai sukupuolta ei voi itse vaihtaa jälkikäteen (pois lukien orien ruunaus) vaan muutos voidaan tehdä tuen kautta.</p>

<h3><a name="syntymaaika"></a>2.3. Syntymäaika</h3>
<p>Hevosen syntymäaika ilmoitetaan rekisteriin muodossa pp.kk.vvvv, esimerkiksi 01.10.2020. Rekisteriin ilmoitetun syntymäajan tulee täsmätä hevosen sivuilla ilmoitettuihin tietoihin, ja se tulee esittää sivuilla muodossa, josta ilmenee päivä, kuukausi ja vuosi, jolloin hevonen on syntynyt. <b>Pelkkä syntymävuosi tai CAS-vuosi hevosen sivuilla ei riitä hevosen rekisteröintiin</b>, vaan tiedossa tulee olla tarkka syntymäaika. </p> 

<p>Et voi rekisteröidä hevosia, jotka eivät ole vielä syntyneet!</p>

<h3><a name="omistaja"></a>2.4. Omistaja</h3>
<p>Hevosen sivulla tulee olla merkittynä hevosen omistajan VRL-tunnus. Voit rekisteröidä vain omia hevosiasi, tunnuksesi merkitään rekisteriin hevosen omistajaksi automaattisesti. Rekisteröinnin jälkeen hevoselle on mahdollista merkitä useita omistajia tai siirtää se toiselle omistajalle hevosen omasta profiilista.</p>

<h2>3. Vapaaehtoiset tiedot</h2>

<h3><a name="vari"></a>3.1. Väri ja säkäkorkeus</h3>
<p>Hevosen värin ja säkäkorkeuden sekä syntymämaan lisääminen rekisterin tietoihin on vapaaehtoista (pl. porrastetuissa kilpailevat hevoset joilta säkäkorkeus vaaditaan). Suosittelemme kuitenkin rodun rotumääritelmään tutustumista ennen hevosen rekisteröintiä, tavallisesti niistä löytyy kriteerit kullakin rodulla sallittuihin väreihin ja tyypilliseen kokoon sekä joissakin tapauksissa syntymä maahan. <b>Ilmoita hevosen säkäkorkeus aikuisena.</b></p>

<h3><a name="kotitalli"></a>3.2. Kotitalli</h3>
<p>Voit halutessasi ilmoittaa hevosen kotitallin. Kotitallia voi vaihtaa myös rekisteröinnin jälkeen.</p>
<p>Täytä lomakkeelle tallin kahdeksanmerkkinen (8) tunniste, muodossa XXXX0000, <b>älä tallin nimeä tai osoitetta!</b></p>

<h3><a name="kasvattaja"></a>3.3. Kasvattaja ja kasvattajaliite</h3>
<p>Jos hevosella on kasvattaja virtuaalimaailmassa, eli hevonen on syntynyt jo olemassa olevista virtuaalihevosista, kasvattajan tiedot (VRL-tunnus tai tallin rekisteritunnus) voi merkitä rekisteröinti lomakkeelle, mikäli kasvattaja ja/tai talli on rekisteröity VRL:oon. Tarkista kasvattajalta vaadittu merkintä! Hevosen sivulle tunnuksia ei tarvitse merkitä, ellei kasvattaja toisin vaadi.</p>

<p>Suvullisen hevosen nimessä oleva kasvattajaliite tulee merkitä erikseen lomakkeelle "Kasvattajaliite"-kenttään, vaikka kasvattajanimeä ei olisi vielä rekisteröity.</p>

<p><b>Suvuttoman hevosen kasvattajaa tai mahdollista keksittyä kasvattajaliitettä ei merkitä rekisteriin.</b></p>

<h3><a name="suku"></a>3.4. Isä ja emä</h3>
<p>Tarkista ennen rekisteröintiä, onko hevosen vanhemmat rekisteröity! <b>Lomakkeelle täytetään vain vanhempien VH-rekisterinumerot</b>. Hevoselle voi lisätä vanhemmat myös jälkikäteen hevosen omasta rekisteriprofiilista.</p>

<p><b>Suvuttomien hevosten vanhempia ei ilmoiteta!</b></p>

<h3><a name="kilpailutiedot"></a>3.5. Kilpailutiedot</h3>
<p>Voit halutessasi ilmoittaa hevosen painotuksen. Painotusta voi vaihtaa myös rekisteröinnin jälkeen.</p>

<p>Valitessasi “hevonen kilpailee porrastetuissa”, lisäät hevosen omille porrastettujen kilpailulistoillesi mikäli sille on merkitty säkäkorkeus ja ikääntyminen eikä se ole aiemmin kilpaillut asetettua tasoaan täyteen. Lue lisää porrastetuista kilpailuista kohdasta Kilpailutoiminta > <a href="<?php echo base_url('kilpailutoiminta/porrastetut')?>" title="Porrastetut kilpailut">Porrastetut kilpailut</a>.</p>

<p>Jos hevosesi kilpailee porrastetuissa etkä halua sen näkyvän jonkin lajin/lajien kilpailulistoilla, valitse lajin pudotusvalikosta tasoksi -1.</p>

<h3><a name="ikaantyminen"></a>3.6. Ikääntyminen</h3>
<p>Ikääntyminen on pakollinen vain porrastetuissa kilpailuissa kilpailevilla hevosilla. Voit ilmoittaa kaikki tai osan syntymäpäivistä myös rekisteröinnin jälkeen tai muokata niitä milloin haluat. Täytä syntymäpäivät rekisteröintilomakkeelle muodossa pp.kk.vvvv, esimerkiksi 01.10.2021.</p>

<p>Ikääntymisen voi poistaa itse poistamalla ilmoitetut syntymäpäivät ja kirjoittamalla "ikääntyminen (päivissä)" kohtaan 00.</p>

<h3><a name="kuollut"></a>3.7. Kuollut</h3>
<p>Voit rekisteröidä myös kuolleita virtuaalihevosia tai merkitä hevosen kuolinpäivän rekisteriin.</p>

